export const LanguageProvider = ({ children }) => {
  // Get saved language from localStorage if available, otherwise default to English
  const [language, setLanguageState] = useState("en");
  const [translations, setTranslations] = useState(languageDictionaries.en || {});

  // Initialize language on component mount
  useEffect(() => {
    const savedLanguage = localStorage.getItem("selectedLanguage") || "en";
    setLanguageState(savedLanguage);
    setTranslations(languageDictionaries[savedLanguage] || languageDictionaries.en || {});
  }, []);

  // Update language and save to localStorage
  const setLanguage = (lang) => {
    if (languageDictionaries[lang]) {
      setLanguageState(lang);
      setTranslations(languageDictionaries[lang]);
      localStorage.setItem("selectedLanguage", lang);

      // Trigger a custom event that can be listened to by the chatbot or other components
      window.dispatchEvent(new CustomEvent("languageChanged", { detail: { language: lang } }));
    }
  };

  // Helper function to get language name from code
  const getLanguageName = (code) => {
    const language = allLanguages.find(lang => lang.code === code);
    return language ? language.name : code;
  };

  // Helper function to check if a language is Indian
  const isIndianLanguage = (code) => {
    return indianLanguages.some(lang => lang.code === code);
  };

  return (
    <LanguageContext.Provider
      value={{
        language,
        setLanguage,
        translations,
        internationalLanguages,
        indianLanguages,
        allLanguages,
        getLanguageName,
        isIndianLanguage
      }}
    >
      {children}
    </LanguageContext.Provider>
  );
};

// Custom hook to use the language context
export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used within a LanguageProvider");
  }
  return context;
};